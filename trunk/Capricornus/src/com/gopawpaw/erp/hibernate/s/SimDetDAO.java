package com.gopawpaw.erp.hibernate.s;

import java.util.List;
import org.apache.commons.logging.Log;
import org.apache.commons.logging.LogFactory;
import org.hibernate.LockMode;
import org.springframework.context.ApplicationContext;
import org.springframework.orm.hibernate3.support.HibernateDaoSupport;

/**
 * A data access object (DAO) providing persistence and search support for
 * SimDet entities. Transaction control of the save(), update() and delete()
 * operations can directly support Spring container-managed transactions or they
 * can be augmented to handle user-managed Spring transactions. Each of these
 * methods provides additional information for how to configure it for the
 * desired type of transaction control.
 * 
 * @see com.gopawpaw.erp.hibernate.s.SimDet
 * @author MyEclipse Persistence Tools
 */

public class SimDetDAO extends HibernateDaoSupport {
	private static final Log log = LogFactory.getLog(SimDetDAO.class);

	protected void initDao() {
		// do nothing
	}

	public void save(SimDet transientInstance) {
		log.debug("saving SimDet instance");
		try {
			getHibernateTemplate().save(transientInstance);
			log.debug("save successful");
		} catch (RuntimeException re) {
			log.error("save failed", re);
			throw re;
		}
	}

	public void delete(SimDet persistentInstance) {
		log.debug("deleting SimDet instance");
		try {
			getHibernateTemplate().delete(persistentInstance);
			log.debug("delete successful");
		} catch (RuntimeException re) {
			log.error("delete failed", re);
			throw re;
		}
	}

	public SimDet findById(com.gopawpaw.erp.hibernate.s.SimDetId id) {
		log.debug("getting SimDet instance with id: " + id);
		try {
			SimDet instance = (SimDet) getHibernateTemplate().get(
					"com.gopawpaw.erp.hibernate.s.SimDet", id);
			return instance;
		} catch (RuntimeException re) {
			log.error("get failed", re);
			throw re;
		}
	}

	public List findByExample(SimDet instance) {
		log.debug("finding SimDet instance by example");
		try {
			List results = getHibernateTemplate().findByExample(instance);
			log.debug("find by example successful, result size: "
					+ results.size());
			return results;
		} catch (RuntimeException re) {
			log.error("find by example failed", re);
			throw re;
		}
	}

	public List findByProperty(String propertyName, Object value) {
		log.debug("finding SimDet instance with property: " + propertyName
				+ ", value: " + value);
		try {
			String queryString = "from SimDet as model where model."
					+ propertyName + "= ?";
			return getHibernateTemplate().find(queryString, value);
		} catch (RuntimeException re) {
			log.error("find by property name failed", re);
			throw re;
		}
	}

	public List findAll() {
		log.debug("finding all SimDet instances");
		try {
			String queryString = "from SimDet";
			return getHibernateTemplate().find(queryString);
		} catch (RuntimeException re) {
			log.error("find all failed", re);
			throw re;
		}
	}

	public SimDet merge(SimDet detachedInstance) {
		log.debug("merging SimDet instance");
		try {
			SimDet result = (SimDet) getHibernateTemplate().merge(
					detachedInstance);
			log.debug("merge successful");
			return result;
		} catch (RuntimeException re) {
			log.error("merge failed", re);
			throw re;
		}
	}

	public void attachDirty(SimDet instance) {
		log.debug("attaching dirty SimDet instance");
		try {
			getHibernateTemplate().saveOrUpdate(instance);
			log.debug("attach successful");
		} catch (RuntimeException re) {
			log.error("attach failed", re);
			throw re;
		}
	}

	public void attachClean(SimDet instance) {
		log.debug("attaching clean SimDet instance");
		try {
			getHibernateTemplate().lock(instance, LockMode.NONE);
			log.debug("attach successful");
		} catch (RuntimeException re) {
			log.error("attach failed", re);
			throw re;
		}
	}

	public static SimDetDAO getFromApplicationContext(ApplicationContext ctx) {
		return (SimDetDAO) ctx.getBean("SimDetDAO");
	}
}