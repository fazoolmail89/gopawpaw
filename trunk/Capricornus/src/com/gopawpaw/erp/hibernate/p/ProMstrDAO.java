package com.gopawpaw.erp.hibernate.p;

import java.util.List;
import org.apache.commons.logging.Log;
import org.apache.commons.logging.LogFactory;
import org.hibernate.LockMode;
import org.springframework.context.ApplicationContext;
import org.springframework.orm.hibernate3.support.HibernateDaoSupport;

/**
 * A data access object (DAO) providing persistence and search support for
 * ProMstr entities. Transaction control of the save(), update() and delete()
 * operations can directly support Spring container-managed transactions or they
 * can be augmented to handle user-managed Spring transactions. Each of these
 * methods provides additional information for how to configure it for the
 * desired type of transaction control.
 * 
 * @see com.gopawpaw.erp.hibernate.p.ProMstr
 * @author MyEclipse Persistence Tools
 */

public class ProMstrDAO extends HibernateDaoSupport {
	private static final Log log = LogFactory.getLog(ProMstrDAO.class);

	protected void initDao() {
		// do nothing
	}

	public void save(ProMstr transientInstance) {
		log.debug("saving ProMstr instance");
		try {
			getHibernateTemplate().save(transientInstance);
			log.debug("save successful");
		} catch (RuntimeException re) {
			log.error("save failed", re);
			throw re;
		}
	}

	public void delete(ProMstr persistentInstance) {
		log.debug("deleting ProMstr instance");
		try {
			getHibernateTemplate().delete(persistentInstance);
			log.debug("delete successful");
		} catch (RuntimeException re) {
			log.error("delete failed", re);
			throw re;
		}
	}

	public ProMstr findById(com.gopawpaw.erp.hibernate.p.ProMstrId id) {
		log.debug("getting ProMstr instance with id: " + id);
		try {
			ProMstr instance = (ProMstr) getHibernateTemplate().get(
					"com.gopawpaw.erp.hibernate.p.ProMstr", id);
			return instance;
		} catch (RuntimeException re) {
			log.error("get failed", re);
			throw re;
		}
	}

	public List findByExample(ProMstr instance) {
		log.debug("finding ProMstr instance by example");
		try {
			List results = getHibernateTemplate().findByExample(instance);
			log.debug("find by example successful, result size: "
					+ results.size());
			return results;
		} catch (RuntimeException re) {
			log.error("find by example failed", re);
			throw re;
		}
	}

	public List findByProperty(String propertyName, Object value) {
		log.debug("finding ProMstr instance with property: " + propertyName
				+ ", value: " + value);
		try {
			String queryString = "from ProMstr as model where model."
					+ propertyName + "= ?";
			return getHibernateTemplate().find(queryString, value);
		} catch (RuntimeException re) {
			log.error("find by property name failed", re);
			throw re;
		}
	}

	public List findAll() {
		log.debug("finding all ProMstr instances");
		try {
			String queryString = "from ProMstr";
			return getHibernateTemplate().find(queryString);
		} catch (RuntimeException re) {
			log.error("find all failed", re);
			throw re;
		}
	}

	public ProMstr merge(ProMstr detachedInstance) {
		log.debug("merging ProMstr instance");
		try {
			ProMstr result = (ProMstr) getHibernateTemplate().merge(
					detachedInstance);
			log.debug("merge successful");
			return result;
		} catch (RuntimeException re) {
			log.error("merge failed", re);
			throw re;
		}
	}

	public void attachDirty(ProMstr instance) {
		log.debug("attaching dirty ProMstr instance");
		try {
			getHibernateTemplate().saveOrUpdate(instance);
			log.debug("attach successful");
		} catch (RuntimeException re) {
			log.error("attach failed", re);
			throw re;
		}
	}

	public void attachClean(ProMstr instance) {
		log.debug("attaching clean ProMstr instance");
		try {
			getHibernateTemplate().lock(instance, LockMode.NONE);
			log.debug("attach successful");
		} catch (RuntimeException re) {
			log.error("attach failed", re);
			throw re;
		}
	}

	public static ProMstrDAO getFromApplicationContext(ApplicationContext ctx) {
		return (ProMstrDAO) ctx.getBean("ProMstrDAO");
	}
}